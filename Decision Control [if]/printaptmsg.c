#include<stdio.h>
void main()
{
  char c;
  printf("Enter the character :");
  scanf("%c",&c);
  if(c=='A')
  {
    printf("\nExcellent");
  }
  if(c=='B')
  {
    printf("\nVery Good");
  }
  if(c=='C')
  {
    printf("\nFair");
  }
  if(c=='D')
  {
    printf("\nPoor");
  }
}
